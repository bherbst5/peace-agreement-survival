if VIOLATORS:
    # Validate every violator name exists in the current design matrix
    for v in VIOLATORS:
        assert v in COVARIATE_NAMES, (
            f"Violator '{v}' not found in COVARIATE_NAMES after interactions "
            f"were added.  Check spelling matches baseline Cox output exactly."
        )

    # log(t_i + 1): +1 guards against t = 0; uses each subject's own time.
    # See module docstring for the approximation note.
    log_t = np.log(t_all + 1)

    tv_resid_list = []
    for v in VIOLATORS:
        j       = COVARIATE_NAMES.index(v)
        col_x   = X_std[:, j]          # already standardised base term
        col_xlt = col_x * log_t        # raw x * log(t)

        # Residualize: project col_xlt onto span{1, col_x}, keep residual.
        # The residual is orthogonal to col_x by construction, so lasso can
        # select/zero x and x*log(t) independently.
        Z                 = np.column_stack([np.ones(n), col_x])
        coefs_tv, _, _, _ = lstsq(Z, col_xlt, rcond=None)
        tv_resid_list.append(col_xlt - Z @ coefs_tv)

    tv_arr  = np.column_stack(tv_resid_list)   # shape (n, n_violators)
    tv_mean = tv_arr.mean(axis=0)
    tv_sd   = tv_arr.std(axis=0);  tv_sd[tv_sd == 0] = 1.0
    tv_std  = (tv_arr - tv_mean) / tv_sd       # standardise to mean-0, sd-1

