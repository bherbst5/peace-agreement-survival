# ═══════════════════════════════════════════════════════════════════════════════
# 2.  CORE HELPER: partial log-likelihood
#     (Breslow, scaled by 1/n_events — identical to custom code)
# ═══════════════════════════════════════════════════════════════════════════════
def partial_loglik(beta, X, t, e):
    """Mean partial log-likelihood (Breslow), scaled by n_events."""
    order   = np.argsort(t)
    X_s     = X[order]; t_s = t[order]; e_s = e[order]
    eta_s   = X_s @ beta
    eta_s  -= eta_s.max()
    exp_s   = np.exp(eta_s)
    cum_exp = np.cumsum(exp_s[::-1])[::-1]
    n_ev    = e_s.sum()
    ll      = 0.0
    for i in range(len(t_s)):
        if e_s[i]:
            ll += eta_s[i] - np.log(cum_exp[i] + 1e-300)
    return ll / (n_ev + 1e-300)

# ═══════════════════════════════════════════════════════════════════════════════
# 3.  λ GRID + SOLUTION PATH (Auto-scaled by scikit-survival)
# ═══════════════════════════════════════════════════════════════════════════════
print("Fitting LASSO path  ...", end="", flush=True)

# Let scikit-survival calculate the optimal data-driven penalty path
m_path = CoxnetSurvivalAnalysis(
    l1_ratio=1.0,
    n_alphas=100,          # Tell it to generate 100 alphas automatically
    alpha_min_ratio=0.01,  # Search down to 1% of the max penalty
    normalize=False,
    fit_baseline_model=False,
    max_iter=100_000,
    tol=1e-8,
)
m_path.fit(X_std, y_surv)

# Extract the automatically generated grid to use in the CV loop
lam_grid = m_path.alphas_
N_LAM = len(lam_grid)

print(" done")
print(f"lambda grid (auto): {lam_grid[0]:.4f} → {lam_grid[-1]:.4f}  ({N_LAM} values)")

# Handle early stopping: pad with the last computed coefficient
path_std = np.zeros((p, len(lam_grid)))
n_fit = m_path.coef_.shape[1]
path_std[:, :n_fit] = m_path.coef_
for j in range(n_fit, len(lam_grid)):
    path_std[:, j] = m_path.coef_[:, -1]

path_orig = path_std / X_sd_full[:, None]   # back-transform to original scale

# ═══════════════════════════════════════════════════════════════════════════════
# 4.  5-FOLD CROSS-VALIDATION  (held-out partial log-likelihood)
#
#     Folds are assigned at the subject level (one row per subject in X_std),
#     which is correct: because the TV terms x_j*log(t_i) are computed from
#     each subject's own time and stored in their row, no leakage can occur
#     across folds.
# ═══════════════════════════════════════════════════════════════════════════════
K         = 5
cv_scores = np.zeros((K, N_LAM))

print(f"\n{K}-fold CV  ...", end="", flush=True)

kf = KFold(n_splits=K, shuffle=True, random_state=42)

for k, (train_idx, val_idx) in enumerate(kf.split(X_std)):
    X_tr = X_std[train_idx];  y_tr = y_surv[train_idx]
    X_va = X_std[val_idx];    t_va = t_all[val_idx]
    e_va = e_float[val_idx]
    
    # Grab training times/events for the Verweij calculation
    t_tr = t_all[train_idx]
    e_tr = e_float[train_idx]

    # Fit the entire sequence of penalties at once for this fold
    m_cv = CoxnetSurvivalAnalysis(
        l1_ratio=1.0,
        alphas=lam_grid,
        normalize=False,
        fit_baseline_model=False,
        max_iter=100_000,
        tol=1e-7,
    )
    m_cv.fit(X_tr, y_tr)
    
    n_fit_cv = m_cv.coef_.shape[1]

    # Evaluate the validation log-likelihood for each penalty level
    for j in range(len(lam_grid)):
        col_idx = min(j, n_fit_cv - 1)
        beta_j = m_cv.coef_[:, col_idx]
        
        # ── THE VERWEIJ FIX (Verweij & van Houwelingen, 1993) ────────────────
        # Calculating Cox partial log-likelihood directly on small validation 
        # folds is heavily biased against non-zero models due to rapidly 
        # shrinking risk sets. 
        # We compute the validation LL using the glmnet standard: apply the 
        # fold's betas to the full dataset, and subtract the log-likelihood 
        # of the training data.
        ll_full  = partial_loglik(beta_j, X_std, t_all, e_float) * e_float.sum()
        ll_train = partial_loglik(beta_j, X_tr, t_tr, e_tr) * e_tr.sum()
        
        cv_scores[k, j] = (ll_full - ll_train) / e_va.sum()

    print(f" fold{k+1}", end="", flush=True)

print(" done")

cv_mean  = cv_scores.mean(axis=0)
cv_se    = cv_scores.std(axis=0) / np.sqrt(K)
best_j   = np.argmax(cv_mean)
lam_best = lam_grid[best_j]
threshold = cv_mean[best_j] - cv_se[best_j]
one_se_j  = np.where(cv_mean >= threshold)[0][0]
lam_1se   = lam_grid[one_se_j]

print(f"\nlambda_min = {lam_best:.5f}  (index {best_j})")
print(f"lambda_1SE = {lam_1se:.5f}  (index {one_se_j})")

