"""
Lasso-Penalized Cox Proportional Hazards Model  —  scikit-survival version
===========================================================================
Direct comparison counterpart to the baseline Cox Proportional Hazards model.

Key design choices that mirror the baseline specification:
  • Same data prep, dummy encoding, and complete suite of interaction terms.
  • Manual Standardization (normalize=False): Predictors are standardized 
    (mean 0, sd 1) manually so coefficients can be directly back-transformed.
  • Auto-Scaled Penalty Grid: The λ (alpha) path is natively generated by 
    scikit-survival (n_alphas=100) to perfectly fit the data's sparsity.
  • Verweij Cross-Validation: Overcomes "Small Risk Set Bias" in Cox CV by 
    calculating held-out partial log-likelihood as the difference between 
    full-dataset and training-dataset likelihoods (the glmnet standard).
  • Bootstrap CIs: 95% Confidence Intervals evaluated at λ_min (B=200).
  • Time-Varying Terms: Evaluated as x_j * log(t_i) and residualized against 
    the base term x_j to ensure orthogonal selection by the LASSO penalty.

Scaling note
------------
scikit-survival scales the loss by 1/n_events. Our Verweij implementation 
re-multiplies by event counts to calculate raw log-likelihoods before 
subtraction, ensuring mathematically unbiased cross-validation.

Requirements
------------
    pip install scikit-survival openpyxl matplotlib pandas numpy
"""

