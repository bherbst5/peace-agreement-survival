# ═══════════════════════════════════════════════════════════════════════════════
# 1c.  TIME-VARYING TERMS FOR PH-VIOLATING COVARIATES
#
#      Variables that fail the PH assumption in the baseline Cox model are
#      entered here as  x_j + x_j*log(t).  The interaction column x_j*log(t)
#      is RESIDUALIZED against x_j before the lasso sees it.
#
#      Why residualize for the lasso specifically?
#      The covariate×covariate interactions above are also residualized, for
#      the same reason: if the lasso zeroes a main effect x_j while retaining
#      x_j*log(t), the surviving interaction coefficient conflates the level
#      of x_j with its time-varying component and becomes uninterpretable.
#      Residualizing x_j*log(t) on [1, x_j] makes the two columns orthogonal
#      so each can be selected or zeroed independently without contaminating
#      the other's estimate.
# ═══════════════════════════════════════════════════════════════════════════════

